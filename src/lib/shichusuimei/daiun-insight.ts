/**
 * 現在の大運期に基づくセクション別コメンタリーを生成する
 * @param period 現在の大運期
 * @param approxAge 現在の概算年齢
 * @param hasExactTime 生まれ時刻が入力されているか
 */
export function getDaiunInsight(
  period: DaiunPeriod,
  approxAge: number,
  hasExactTime: boolean,
): DaiunInsight {
  const element = STEM_ELEMENTS[period.stem];
  const insights = ELEMENT_INSIGHTS[element];
  const timeNote = hasExactTime
    ? ''
    : '時刻は正午で概算しているため、大運の起算年齢に±1〜2年の誤差があります。';

  return {
    currentTheme: insights.themeSummary,
    element,
    career: insights.career,
    money: insights.money,
    turningPoint: insights.turningPoint,
    growth: insights.growth,
    lifeDirection: insights.lifeDirection,
    timeNote,
  };
}
