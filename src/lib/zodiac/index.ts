const SIGN_ORDER: SignKey[] = ['牡羊座', '牡牛座', '双子座', '蟹座', '獅子座', '乙女座', '天秤座', '蠍座', '射手座', '山羊座', '水瓶座', '魚座'];

function getZodiacIndex(month: number, day: number): number {
  if ((month === 3 && day >= 21) || (month === 4 && day <= 19)) return 0;
  if ((month === 4 && day >= 20) || (month === 5 && day <= 20)) return 1;
  if ((month === 5 && day >= 21) || (month === 6 && day <= 21)) return 2;
  if ((month === 6 && day >= 22) || (month === 7 && day <= 22)) return 3;
  if ((month === 7 && day >= 23) || (month === 8 && day <= 22)) return 4;
  if ((month === 8 && day >= 23) || (month === 9 && day <= 22)) return 5;
  if ((month === 9 && day >= 23) || (month === 10 && day <= 23)) return 6;
  if ((month === 10 && day >= 24) || (month === 11 && day <= 22)) return 7;
  if ((month === 11 && day >= 23) || (month === 12 && day <= 21)) return 8;
  if ((month === 12 && day >= 22) || (month === 1 && day <= 19)) return 9;
  if ((month === 1 && day >= 20) || (month === 2 && day <= 18)) return 10;
  return 11;
}

function getDecanIndex(month: number, day: number, signIndex: number): number {
  const decans = ZODIAC_DATA[SIGN_ORDER[signIndex]].decans;
  for (let i = decans.length - 1; i >= 0; i--) {
    const [startStr] = decans[i].dateRange.split('〜');
    const [dm, dd] = startStr.split('/').map(Number);
    if (month > dm || (month === dm && day >= dd)) return i;
  }
  return 0;
}

export function calculateZodiac(month: number, day: number): ZodiacResult {
  const signIndex = getZodiacIndex(month, day);
  const signKey = SIGN_ORDER[signIndex];
  const base = ZODIAC_DATA[signKey];
  const decanIdx = getDecanIndex(month, day, signIndex);
  const decan = base.decans[decanIdx];

  return {
    sign: signKey,
    emoji: base.emoji,
    element: base.element,
    quality: base.quality,
    rulingPlanet: base.rulingPlanet,
    dateRange: base.dateRange,
    title: base.title,
    description: base.description,
    temperament: base.temperament,
    behaviorPattern: base.behaviorPattern,
    interpersonal: base.interpersonal,
    planetInfluence: base.planetInfluence,
    decan: {
      number: decanIdx + 1,
      subRuler: decan.subRuler,
      dateRange: decan.dateRange,
      description: decan.description,
    },
    strengths: base.strengths,
    weaknesses: base.weaknesses,
    compatibility: base.compatibility,
    luckyColor: base.luckyColor,
    luckyNumber: base.luckyNumber,
    careerAnalysis: base.careerAnalysis,
    suitableCareers: base.suitableCareers,
    leadershipStyle: base.leadershipStyle,
    hiddenTalent: base.hiddenTalent,
  };
}

