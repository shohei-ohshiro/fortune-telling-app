/**
 * 生年月日からキャラナンバー（1〜60）を算出する。
 * 六十干支の日周期に基づく計算。
 */
function getCharacterNumber(year: number, month: number, day: number): number {
  const utcMs = Date.UTC(year, month - 1, day);
  const unixDays = Math.floor(utcMs / 86400000);
  return ((unixDays + 25577) % 60 + 60) % 60 + 1;
}

/**
 * 生年月日から動物占いの結果を算出する
 */
export function calculateAnimal(year: number, month: number, day: number): AnimalResult {
  const charNum = getCharacterNumber(year, month, day);
  const character = CHARACTER_TABLE[charNum - 1];
  const base = BASE_ANIMALS[character.animal];

  return {
    animal: character.animal,
    emoji: base.emoji,
    color: base.color,
    title: character.name,
    characterNumber: charNum,
    description: base.description,
    temperament: base.temperament,
    behaviorPattern: base.behaviorPattern,
    interpersonal: base.interpersonal,
    uniqueDescription: character.uniqueDescription,
    strengths: base.strengths,
    weaknesses: base.weaknesses,
    compatibility: base.compatibility,
    careerAnalysis: base.careerAnalysis,
    suitableCareers: base.suitableCareers,
    leadershipStyle: base.leadershipStyle,
  };
}

export { CHARACTER_TABLE, BASE_ANIMALS };
